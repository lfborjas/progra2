    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Libro l = new Libro(txtName.getText()
                                       , new GregorianCalendar(
                                           ((Integer)cmbYear.getSelectedItem())
                                           ,((Integer)cmbMonth.getSelectedItem())
                                           ,((Integer)cmbDay.getSelectedItem())
                                       ));
        if(!editing){
            bookModel.addElement(l);
        }else{
            int index = this.lstLibros.getSelectedIndex();
            bookModel.removeElementAt(index);
            bookModel.add(index, l);
            editing = false;
            this.jButton1.setText("Agregar");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void lstLibrosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstLibrosValueChanged
        Libro l = (Libro)this.lstLibros.getSelectedValue();
        if (l!= null){
        this.txtName.setText(l.nombre);
        this.cmbYear.setSelectedItem(l.fechaPub.get(Calendar.YEAR));
        this.cmbMonth.setSelectedItem(l.fechaPub.get(Calendar.MONTH));
        this.cmbDay.setSelectedItem(l.fechaPub.get(Calendar.DAY_OF_MONTH));
        this.jButton1.setText("Actualizar");
        editing = true;
        }
    }//GEN-LAST:event_lstLibrosValueChanged

