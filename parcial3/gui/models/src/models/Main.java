    static class Libro {

        String nombre;
        GregorianCalendar fechaPub;

        public Libro(String n, GregorianCalendar f) {
            this.nombre = n;
            this.fechaPub = f;
        }

        @Override
        public String toString() {
            return String.format("'%s', publicado hace %d años",
                    this.nombre,
                    ((new GregorianCalendar()).get(Calendar.YEAR))-fechaPub.get(Calendar.YEAR));
        }
    }

    public static void fillModel(DefaultComboBoxModel model, final int begin,
                                final int end){
       for(int i = begin ; i < end ; i++)
           model.addElement(i);
    }
